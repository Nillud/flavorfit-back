import { Injectable, NotFoundException } from '@nestjs/common'
import { PrismaService } from 'src/prisma/prisma.service'
import type { RecipesQueryInput } from './inputs/get-recipes-query.input'
import { Prisma } from 'prisma/generated/prisma/client'

@Injectable()
export class RecipesService {
	constructor(private readonly prisma: PrismaService) {}

	async getAll({ page, limit, searchTerm, sort }: RecipesQueryInput) {
		const skip = (page - 1) * limit

		const data = await this.prisma.recipe.findMany({
			skip,
			take: limit,

			where: {
				...(searchTerm && {
					OR: [
						{ title: { contains: searchTerm, mode: 'insensitive' } },
						{ description: { contains: searchTerm, mode: 'insensitive' } },
						{
							recipeIngredients: {
								some: {
									ingredient: {
										name: {
											contains: searchTerm,
											mode: 'insensitive'
										}
									}
								}
							}
						}
					]
				})
			},

			orderBy: this.getOrderBy(sort),

			include: {
				comments: true,
				likes: true
			}
		})

		return data
	}

	private getOrderBy(sort?: string) {
		switch (sort) {
			case 'recommended':
				return { likes: { _count: Prisma.SortOrder.desc } }

			case 'popular':
				return { views: Prisma.SortOrder.desc }

			default:
				return { createdAt: Prisma.SortOrder.desc }
		}
	}

	async getBySlug(slug: string) {
		const recipe = await this.prisma.recipe.findUnique({
			where: { slug },
			include: {
				recipeSteps: true,
				recipeIngredients: {
					include: {
						ingredient: true
					}
				}
			}
		})
		if (!recipe)
			throw new NotFoundException(`recipe with slug ${slug} not found`)
		return recipe
	}
}
